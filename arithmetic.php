<?php

function add($a, $b)
{
    return $a + $b;
}

function subtract($a, $b)
{
    return $a - $b;
}

function multiply($a, $b)
{
    return $a * $b;
}

function divide($a, $b)
{
    return $a / $b;
}

echo add(3, 4) . PHP_EOL;

echo subtract(16, 7) . PHP_EOL;

echo multiply(200, 4) . PHP_EOL;

echo divide(25, 5) . PHP_EOL;
